    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //select from combobox which calcuation to do and call the appropriate object
        if(jComboBox1.getSelectedItem().toString() == "Number To Bits"){

            try{
                //get string from input field
                String sInput = jTextField1.getText();
                //remove whitespace
                String noWhite = sInput.replaceAll("\\s", "");
                //parse string to integer
                int input = Integer.parseInt(noWhite);
                //check if below or equal to 255
                if(input <= 255) {
                    //if input is correct then create the numberToBits object
                    NumberToBits numberToBits = new NumberToBits(input);
                    //call the convertNumberToBits method
                    String bitsAnswer = numberToBits.convertNumberToBits(input);
                    //print answer to text field
                    jTextField2.setText(bitsAnswer);
                }else{
                    jTextField2.setText("input needs to be <= 255");
                }
            //alert the user if input is incorrect
            }catch(NumberFormatException ex) {
                jTextField2.setText("Must use an integer");
            }
        }else{
            //check for NumberFormatException
            try{
                //get string from input field
                String sInput = jTextField1.getText();
                //remove whitespace
                String noWhite = sInput.replaceAll("\\s", "");
                //check if input exceeds or is less than 8 bits
                if(noWhite.length() > 8 || noWhite.length() < 8){
                    //alert the user if input is incorrect
                    jTextField2.setText("Input must contain 8 bits");
                }else{
                    //if the input is valid then create the BitsToNumber object
                    BitsToNumber bitsToNumber = new BitsToNumber(noWhite);
                    //call the converBitsoNumber method
                    String numbersToBits = bitsToNumber.convertBitsToNumber(noWhite);
                    //print answer to text field
                    jTextField2.setText(numbersToBits);
                }

            }catch(NumberFormatException ex) {
                jTextField2.setText("Must use an integer");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

